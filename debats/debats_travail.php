    <div class="col-xs-12">
      Un thème récurrent dans le discours politique est celui d’ « assistanat », il consiste à dénigrer une proposition d’aide sociale au motif que des gens en profiteraient pour ne rien faire. Beaucoup de chômeurs sont ainsi stigmatisés et ne font pas les démarches nécessaires pour toucher les allocations auxquelles ils sont éligibles. Ainsi, 50 % des personnes éligibles au RSA n’en font pas la demande, car elle est très intrusive et peut être vécue comme humiliante puisqu’il s’agit de prouver à l’Etat que l’on est pauvre, ce chiffre monte à 68 % pour le RSA activité.
      Le revenu de base permettrait à toutes les personnes dans le besoin d’être aidées inconditionnellement, cependant il ne changerait pas la perception des chômeurs puisqu’une des critiques les plus fréquentes du revenu de base est que les gens n’accepteront pas de payer pour des personnes qui font le choix de ne pas travailler, aussi rares soient-elles.
